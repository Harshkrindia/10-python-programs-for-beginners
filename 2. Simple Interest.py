r = float(input("Enter the RATE of interest:"))
t = int(input("Enter the time(in years) :"))
#now use the formula and print the values
si = (p*r*t)/100
print("The Simple Interest for", p, "ammount with ", r,"% rate of intrest for the duration ", t, "years is = ", si)

